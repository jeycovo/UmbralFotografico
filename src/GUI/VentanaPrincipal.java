    private void abrirArchivoMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrirArchivoMenuActionPerformed
         FileFilter filtro;
        
        filtro = new FileNameExtensionFilter("Image files", ImageIO.getReaderFileSuffixes());
        fc.addChoosableFileFilter(filtro);
        fc.setFileFilter(filtro);   
        /* 
        filtro = new FileNameExtensionFilter("PNG ficheros", "png");
        fc.addChoosableFileFilter(filtro);
        */
        int res = fc.showOpenDialog(null);
        if(res == JFileChooser.APPROVE_OPTION) {
            System.out.println("Se ha seleccionado un archivo");
            fichero = fc.getSelectedFile();
            System.out.println("Se ha seleccionado el fichero: " + fichero.getName());
            initializeImage();

        }else{
            System.out.println("No se ha seleccionado nada.");
        }
        
    }//GEN-LAST:event_abrirArchivoMenuActionPerformed
private void initializeImage() {
        try {
            image = ImageIO.read(fichero);
        } catch (IOException ex) {
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        utilidadPanel.setImag(image);
        utilidadPanel.repaint();
    }
